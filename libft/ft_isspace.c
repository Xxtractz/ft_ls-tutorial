#include "../includes/libft.h"

int	ft_isspace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n' || c == '\a'
	|| c == '\b' || c == '\f' || c == '\r' || c == '\v')
	{
		return (1);
	}
	else
		return (0);
}
